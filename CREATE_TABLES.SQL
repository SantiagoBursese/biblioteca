
CREATE TABLE IF NOT EXISTS AUTOR(
 CODIGO INT PRIMARY KEY,
 NOMBRE VARCHAR(255) NOT NULL,
 APELLIDO VARCHAR(255) NOT NULL
);

CREATE TABLE IF NOT EXISTS GENERO(
 CODIGO INT PRIMARY KEY,
 NOMBRE VARCHAR(255) NOT NULL
);

CREATE TABLE IF NOT EXISTS EDITORIAL(
 CODIGO INT PRIMARY KEY,
 NOMBRE VARCHAR(255) NOT NULL
);

CREATE TABLE IF NOT EXISTS LIBRO(
 CODIGO INT PRIMARY KEY ,
 NOMBRE VARCHAR(255) NOT NULL,
 CODIGOEDITORIAL BIGINT,
 CODIGOGENERO BIGINT,
 CODIGOAUTOR BIGINT
);

CREATE TABLE IF NOT EXISTS SOCIO(
 CODIGO INT PRIMARY KEY ,
 NOMBRE VARCHAR(255) NOT NULL,
 APELLIDO VARCHAR(255) NOT NULL, 
 DNI VARCHAR(255) NOT NULL,
 TELEFONO VARCHAR(255) NOT NULL
);


alter table LIBRO
  add constraint FK_CODIGO_AUTOR
  foreign key (CODIGOAUTOR)
  references AUTOR (CODIGO);
  
alter table LIBRO
  add constraint FK_CODIGO_GENERO
  foreign key (CODIGOGENERO)
  references GENERO (CODIGO);
  
alter table LIBRO
  add constraint FK_CODIGO_EDITORIAL
  foreign key (CODIGOEDITORIAL)
  references EDITORIAL (CODIGO);